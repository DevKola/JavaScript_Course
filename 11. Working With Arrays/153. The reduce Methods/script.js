const movements = [200, 450, -400, 3000, -650, -130, 70, 1300];

// These methods are used for data ttransformation. Basically we use them to create new arrays based on data transformation from new arrays: Map, Filter, Reduce



// Reduce: This is used to boil down all the elements in the original array to one single array. (e.g adding all elements together) A good example will be to add all the elements of an array together. For this method, we will have an accumulator variable that will hold the total sum of all the elements as the resuce method loops through all the elements in the array and add them together. this process reduce the array to one single value, which is the sum of all the elements and it's this value that will get return
// In the reduce callback function, the first parameter is an accumulator. this accumulator parameter will keep accumulating the result of the value we want to return

const balance = movements.reduce((acc, cur) => acc + cur, 0);
console.log(balance);


// Maximum value of the array
const max = movements.reduce((acc, mov) => {
  if (acc > mov) {
    return acc;
  } else {
    return mov;
  }
}, movements[0]);
